package MasterClass.Composition.Challenger;

public class Ceiling {
    private int height;
    private String paindetColor;

    public Ceiling(int height, String paindetColor) {
        this.height = height;
        this.paindetColor = paindetColor;
    }

    public int getHeight() {
        return this.height;
    }

    public String getPaindetColor() {
        return this.paindetColor;
    }
}
